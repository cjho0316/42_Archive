#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*p;

	p = (t_list *)malloc(sizeof(t_list));
	if (p == 0)
		return (0);
	p->next = 0;
	p->content = content;
	return (p);
}
