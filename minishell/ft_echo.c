#include "minishell.h"

void	ft_echo(char **cmd)
{
	if (cmd[0] == '-')
	{
		if (cmd[0][1] == 'n')
			printf("%s", cmd[1]);
	}
	else
		printf("%s\n", cmd[1]);
}
