#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	i;
	size_t	temp;

	i = 0;
	temp = ft_strlen(src);
	if (!dst || !src)
		return (0);
	while (i < temp && i + 1 < dstsize)
	{
		dst[i] = src[i];
		i++;
	}
	if (dstsize > 0)
		dst[i] = 0;
	return (temp);
}
