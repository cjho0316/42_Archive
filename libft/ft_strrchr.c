char *strrchr(const char *s, int c)
{
    char *pvt;
    
    pvt = 0;

    if (s != 0)
    {
        pvt = s;
        while (s)
        {
            if (s == (char)c)
                *pvt = *s;
            *s++;
        }
    }
    return(pvt);
}